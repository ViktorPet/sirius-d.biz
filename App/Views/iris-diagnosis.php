                    <p>
                        Един жрец вървял в гората и хванал бухал. При схватката помежду им наранил крака на птицата и забелязал как в окото й се появила една черна черта.
                        Първите исторически данни за ирисовата диагностика идват от древния Египет. Придворният лекар на фараона жрец Ел Акс се славел като откривател на иридофотографията. Той доближавал до окото на болния метална плочка, химически обработена, задържал я
                        няколко минути и след това получавал образ на самото око. Незнайни са все още химическите вещества, които използвал древният учен, незнаен е и механизмът на работата му, но мълвата за чудесата, които извършвал, наричани тогава "божествена наука", все още е жива.
                        Такива плочки с фотография на ирис са открити в гробницата на Тутанкамон. Естествено, те представят окото на великия фараон. От тях се разбира, че той е бил много болнав и може би за това е починал на 15 години, което пак се разбира от ириса му.
                        От древния Египет "божествената наука" се разпространява от учени, жреци, йоги и лами във Вавилон, Индия, Тибет и Китай.
                        Тайната на жреца Ел Акс е съхранена върху два папироса с дължина по 30 метра и широчина 1,50 м. Съществуването им е документирано в древни писмени източници, но все още оригиналите не са открити. Информация за тяхното съществуване е имало и във Вавилонската библиотека. Затова археолозите продължават търсенията и ако открият папирус, ще бъде една от най-големите сензации в науката.
                    </p>
                    <p>
                        "Божествената наука" в древността се е пазела в тайна. От нея са се възползвали само аристократите, фараоните, посветените.
                        Днес, ако древните източници видят бял свят, тя ще бъде достъпна до повече хора.
                    </p>
                    <p>
                        Основоположник на съвременната ирисова диагностика е унгарецът д-р Игнац фон Пекчели. Той е роден в Будапеща през 1826 г.
                        Като малко момче той се разхождал в градината на имението на баба си, богати аристократи били фон Пекчели. И му се случило подобно нещо като на древния жрец. Игнац видял една сова и като всеки малчуган решил да я хване. И както се случило преди стотици години, счупил крака й при схватката. Момчето забелязало, че веднага след травмата на пернатото, в окото му се появила черна точка. Детето започнало да се грижи за птицата и да я наблюдава. Забелязало, че след като крачето заздравяло, тази черна точка в окото й, изчезнала.
                        След време, дали заради случката от детството, или по семейна традиция, Игнац фон Пекчели завършил традиционна медицина. Като доктор, добил известност и стабилна практика, той разбрал, че съвременните методи не изчерпват всички възможности за диагностициране.
                        Гледал очите, ирисите на болните. Наблюдавал и размишлявал. Не бил запознат с древните източници за "божествената наука". Сам разкрил връзката между ирисовите знаци и вътрешните органи. И първи направил карта на ириса с разположението на органите върху него. Плод на неговия труд е "Ръководство за изучаване диагнозата по очите" през 1881 г. Книгата се посрещнала с ледено мълчание от научните и медицинските среди. Само признателните му болни възхвалявали изключителната му диагностика. Държавата взела мерки, за да спре дейността му с указ. Но тогава се намесила и пресата. За уникалния доктор и начина му на установяване на болестите се чуло в съседните страни. Отричането на работата му в Унгария се превърнало в печеливша реклама.
                        Той станал известен в Европа. До края на живота си (1907г.) д-р Игнац фон Пекчели продължил изследванията и проучванията си върху ириса.
