                    <h3 style="text-align: center">БИОРЕЗОНАСНА НЕЛИНЕЙНА ДИАГНОСТИКА с апарат „Нейро Оберон“</h3>
                    <p style="text-align: center"><small>(статията е авторска и всяко повторение в пресата или интернет е кражба)</small></p>
                    <p style="text-indent: 7.1pt; line-height: normal;"><span style="font-size: 12pt; font-family: 'Arial','sans-serif';">В днешно време биорезонансната диагностика е най-точната технология за определяне на проблеми и заболявания в организма. Тази диагностика е основана на ефекта на резонанса. Накратко, <strong><span style="color: #c00000;">резонанс</span></strong> е усилване на сигнал при наслагване на електромагнитни, звукови и др. вълни. С помощта на биорезонансната диагностика е възможно да се установи всяка инфекция в който и да е орган. Възможно е да се улови заболяване още на етапа на неговото образуване, при което другите стандартни методи като рентген, ултразвукова диагностика, ЯМР и други, не могат да го диагностицират. Това е много важно, например, при онкологични заболявания.</span></p>
                    <p style="text-indent: 7.1pt; line-height: normal;"><span style="font-size: 12pt; font-family: 'Arial','sans-serif';">C помощта на този ефективен метод може комплексно и пълно да се изследва състоянието на организма. Това изследване заменя посещението при над 20 здравни специалисти и десетки лабораторни анализи. Лабораторните анализи улавят вече изявено и напреднало заболяване! В медицинската практика всички изследвания на човешкия организъм са <strong>инвазивни</strong>, т.е. има инвазия - навлизане на интрумент и/или вещество в човешкия организъм, които винаги са със странични явления и крият рискове. Така например: катетеризация – вкарва се катетър, биопсия – взема се проба от жива тъкан посредством изрязване, ангио и други графии, при които се вкарва микросонда в кръвоносни съдове и контрасни вещества – все процедури с висок риск и за здрав организъм. Можем да посочим и други популярни изследвания, които не просто крият риск, а носят високи рискове, като рентгеновата диагностика, радиоизотопната диагностика и компютърната томография, които облъчват организма с големи дози опасно лъчение. При <strong>неинвазивната</strong> диагностика, каквато е биорезонансната диагностиката при Нейро-Оберон, няма никакво навлизане в човешкия организъм на инструментариум за диагностика и вещества със странични явления, няма вредно облъчване и зареждане на организма с радиоактивни бавноразпадащи се вещества, водещи до деструктивни промени на самата тъкан на клетъчно ниво.</span></p>
                    <p style="text-indent: 7.1pt; line-height: normal;"><span style="font-size: 12pt; font-family: 'Arial','sans-serif';">При сегашната практика често е необходимо да се правят многократни анализи, за да се установи каква е реалната вирусна, бактериална и паразитна флора в организма. Нейро-Оберон фиксира наличието на всички обитаващи територията на организма неканени гости като хелминти (глисти, аскариди, нематоди), патогенни гъбички с различна локализация (кожа, нокти, вътрешни органи, мускулни влакна, кости, дори и в мозъка) и други групи организми, известни на микробиологията.</span></p>
                    <p style="text-indent: 7.1pt; line-height: normal;"><span style="font-size: 12pt; font-family: 'Arial','sans-serif';">Обикновено тестовете за алергени не дават точна диагноза, трябва многократното да се повтарят. Биорезонансната диагностика веднага установява алергени, дори и такива, които стандартната алергология не отчита – като полиетилен, полистирол и други изкуствени вещества, които присъстват изобилно в ежедневието ни. Често точно такива ежедневни отрови водят до заболявания, даващи общи симптоми, които объркват диагностицирането. Такива скрити заболявания хронифицират нелекувани.</span></p>
